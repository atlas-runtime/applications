void
qjs_execute_code(char *buffer, size_t buffer_len)
{
    eval_buf(quickjs_enclave_ctx, buffer, buffer_len, "<input>", JS_EVAL_TYPE_GLOBAL);
}

extern const char *srl_code;

int
bootstrap_qjs()
{
    // in case there is another instance already available, destroy it
    if (quickjs_enclave_ctx != NULL) {
        js_std_free_handlers(quickjs_enclave_rt);
        JS_FreeContext(quickjs_enclave_ctx);
        JS_FreeRuntime(quickjs_enclave_rt);
    }
    JSRuntime *rt;
    JSContext *ctx;
    struct trace_malloc_data trace_data = { NULL };
    size_t memory_limit = 0;
    size_t stack_size = 0;
    rt = JS_NewRuntime();
    if (!rt) {
        abort();
        fprintf(stderr, "qjs: cannot allocate JS runtime\n");
        exit(2);
    }
    if (memory_limit != 0)
        JS_SetMemoryLimit(rt, memory_limit);
    if (stack_size != 0)
        JS_SetMaxStackSize(rt, stack_size);
    js_std_set_worker_new_context_func(JS_NewCustomContext);
    js_std_init_handlers(rt);
    ctx = JS_NewCustomContext(rt);
    if (!ctx) {
        abort();
        fprintf(stderr, "qjs: cannot allocate JS context\n");
        exit(2);
    }
    /* loader for ES6 modules */
    JS_SetModuleLoaderFunc(rt, NULL, js_module_loader, NULL);
    js_std_add_helpers(ctx, 0, NULL);
    // we don't have bignum enabled so we have to inject the buffer as executable code
    eval_buf(ctx, srl_code, strlen(srl_code), "<input>", JS_EVAL_TYPE_MODULE);
    FILE *p = fopen("evaluator.js", "r");
    evaluator = calloc(1, 10000);
    fread(evaluator, 10000, 1, p);
    fclose(p);
    evaluator_len = strlen(evaluator);
    quickjs_enclave_ctx = ctx;
    quickjs_enclave_rt = rt;
    return 1;
}
